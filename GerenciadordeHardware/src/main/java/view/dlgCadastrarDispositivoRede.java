    public void habilitarCampos(boolean flag) {
        for (int i = 0; i < panPreencher.getComponents().length; i++) {
            panPreencher.getComponent(i).setVisible(flag);
        }
    }
    
    public void limparCampos() {
        edtNome.setText("");
        edtFabricante.setText("");
        edtModelo.setText("");
        fEdtPreco.setText("");
        fEdtIP.setText("");
    }
    
    public void preencherFormulario(DispositivoDeRede dr) {
        edtNome.setText(dr.getNome());
        edtFabricante.setText(dr.getFabricante());
        edtModelo.setText(dr.getModelo());
        fEdtPreco.setText(String.valueOf(dr.getPreco()));
        fEdtIP.setText(dr.getEnderecoIP());
        edtCodigo.setText(dr.getCodigo());
        edtDescricao.setText(dr.getDescricao());
        fEdtDataFabricacao.setText(dr.getDataFabricacao());
        edtEstoque.setText(String.valueOf(dr.getEstoque()));
        edtCategoria.setText(dr.getCategoria());
    }
    
    private void criarMascaraCampos(){
        try {
            MaskFormatter ipMask = new MaskFormatter("###.###.##.#");
            ipMask.install(fEdtIP);
            MaskFormatter datFabricacaoMask = new MaskFormatter("##/##/####");
            datFabricacaoMask.install(fEdtDataFabricacao);
            MaskFormatter precoMask = new MaskFormatter("####.##");
            precoMask.install(fEdtPreco);
        } catch (ParseException ex) {
            Logger.getLogger(dlgCadastrarDispositivoRede.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    private Object getObjetoSelecionadoNaGrid() {
        int rowCliked = grdDispositivosRede.getSelectedRow();
        Object obj = null;
        if (rowCliked >= 0) {
            obj = grdDispositivosRede.getModel().getValueAt(rowCliked, -1);
        }
        return obj;
    }


